package firstday;
class looping{

    public static void main(String []args){

        //for loop 
        for(int i=0;i<10;i++){

            System.out.println("Iteration :-"+i);
        }

        //while loop
        int j=0;
        while(j<10){

            System.out.println("Iteration :-"+j);
            j++;
        }
    }
}